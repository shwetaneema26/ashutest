package ashutest;

public class Order {
    private int price ;
    private String symbol;

    public int getPrice() {
        return price;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }
}
